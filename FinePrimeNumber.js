function primeNumber(){
    let n=20
    for(i=2;i<n;i++){
        let isPrime=true
        for(j=2;j<=Math.sqrt(i);j++){
            if(i%j==0){
                isPrime=false
                break
            }
        }
        if(isPrime){
            console.log(i)
        }
    }
}
